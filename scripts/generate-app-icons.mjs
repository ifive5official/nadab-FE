import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const rawIconsDir = path.join(projectRoot, "src/assets/icons/raw");
const generatedDir = path.join(projectRoot, "src/assets/icons/generated");
const outputFile = path.join(generatedDir, "appIconAssets.tsx");

const jsxAttributeNames = new Map([
  ["clip-path", "clipPath"],
  ["clip-rule", "clipRule"],
  ["fill-rule", "fillRule"],
  ["stroke-linecap", "strokeLinecap"],
  ["stroke-linejoin", "strokeLinejoin"],
  ["stroke-miterlimit", "strokeMiterlimit"],
  ["stroke-width", "strokeWidth"],
  ["stroke-dasharray", "strokeDasharray"],
  ["stroke-dashoffset", "strokeDashoffset"],
  ["stop-color", "stopColor"],
  ["stop-opacity", "stopOpacity"],
  ["fill-opacity", "fillOpacity"],
  ["stroke-opacity", "strokeOpacity"],
  ["class", "className"],
  ["xlink:href", "xlinkHref"],
  ["xmlns:xlink", "xmlnsXlink"],
]);

const tokenizedPaintValues = new Set([
  "#17191a",
  "#1b1c2f",
  "#000",
  "#000000",
]);

function toIconName(fileName) {
  return path
    .basename(fileName, ".svg")
    .trim()
    .toLowerCase()
    .replace(/[_\s]+/g, "-")
    .replace(/[^a-z0-9-]/g, "")
    .replace(/-{2,}/g, "-")
    .replace(/^-|-$/g, "");
}

function toJsxAttributeName(name) {
  return jsxAttributeNames.get(name) ?? name;
}

function normalizePaintAttributes(svg) {
  return svg.replace(/\b(fill|stroke)="([^"]+)"/gi, (match, attr, value) => {
    if (tokenizedPaintValues.has(value.toLowerCase())) {
      return `${attr}="currentColor"`;
    }

    return match;
  });
}

function toJsxSvgMarkup(svg) {
  return normalizePaintAttributes(svg)
    .replace(/<!--[\s\S]*?-->/g, "")
    .replace(/\sxmlns(:xlink)?="[^"]*"/g, "")
    .replace(/\sxml:space="preserve"/g, "")
    .replace(/\b([a-zA-Z_:][a-zA-Z0-9_:.-]*)(=)/g, (match, name, equals) => {
      return `${toJsxAttributeName(name)}${equals}`;
    });
}

function extractSvgParts(filePath, svg) {
  if (/data:image\//i.test(svg)) {
    return {
      skippedReason: "contains embedded raster image data",
    };
  }

  const match = svg.match(/<svg\b([^>]*)>([\s\S]*?)<\/svg>/i);

  if (!match) {
    return {
      skippedReason: "does not contain a valid svg root",
    };
  }

  const [, attrs, children] = match;
  const viewBox = attrs.match(/\bviewBox="([^"]+)"/)?.[1];

  if (!viewBox) {
    return {
      skippedReason: "does not contain a viewBox",
    };
  }

  return {
    viewBox,
    children: toJsxSvgMarkup(children.trim()),
  };
}

async function getSvgFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const entryPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      files.push(...(await getSvgFiles(entryPath)));
      continue;
    }

    if (entry.isFile() && entry.name.endsWith(".svg")) {
      files.push(entryPath);
    }
  }

  return files.sort((a, b) => a.localeCompare(b));
}

function indentSvgChildren(children) {
  return children
    .split("\n")
    .filter((line) => line.trim().length > 0)
    .map((line) => `      ${line}`)
    .join("\n");
}

function renderAssetEntry(name, viewBox, children) {
  return `  ${JSON.stringify(name)}: ({ title, ...props }: AppIconSvgProps) => (
    <svg viewBox=${JSON.stringify(viewBox)} fill="none" {...props}>
      {title ? <title>{title}</title> : null}
${indentSvgChildren(children)}
    </svg>
  ),`;
}

function renderGeneratedFile(entries) {
  return `// This file is generated by scripts/generate-app-icons.mjs.
// Do not edit by hand.

import type { ReactElement, SVGProps } from "react";

export type AppIconSvgProps = Omit<SVGProps<SVGSVGElement>, "children"> & {
  title?: string;
};

export const iconAssets = {
${entries.join("\n")}
} satisfies Record<string, (props: AppIconSvgProps) => ReactElement>;

export type AppIconName = keyof typeof iconAssets;
`;
}

const svgFiles = await getSvgFiles(rawIconsDir);
const usedNames = new Set();
const entries = [];
const skipped = [];

for (const filePath of svgFiles) {
  const relativePath = path.relative(projectRoot, filePath);
  const baseName = toIconName(filePath);
  const svg = await readFile(filePath, "utf8");
  const svgParts = extractSvgParts(filePath, svg);

  if (svgParts.skippedReason) {
    skipped.push(`${relativePath}: ${svgParts.skippedReason}`);
    continue;
  }

  if (!baseName) {
    skipped.push(`${relativePath}: could not derive an icon name`);
    continue;
  }

  let iconName = baseName;
  let duplicateIndex = 2;

  while (usedNames.has(iconName)) {
    iconName = `${baseName}-${duplicateIndex}`;
    duplicateIndex += 1;
  }

  usedNames.add(iconName);
  entries.push(renderAssetEntry(iconName, svgParts.viewBox, svgParts.children));
}

await mkdir(generatedDir, { recursive: true });
await writeFile(outputFile, renderGeneratedFile(entries), "utf8");

console.log(`Generated ${entries.length} app icons at ${path.relative(projectRoot, outputFile)}.`);

if (skipped.length > 0) {
  console.warn(`Skipped ${skipped.length} SVG file(s):`);
  for (const message of skipped) {
    console.warn(`- ${message}`);
  }
}
